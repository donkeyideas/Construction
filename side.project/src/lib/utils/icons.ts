/** Map a navigation label to an icon name */
export function inferIcon(label: string): string {
  const lower = label.toLowerCase()
  const map: [string[], string][] = [
    [['home', 'dashboard', 'main', 'start'], 'home'],
    [['about', 'company', 'who', 'story'], 'user'],
    [['service', 'what we do', 'offerings'], 'tool'],
    [['product', 'shop', 'store', 'catalog'], 'package'],
    [['contact', 'reach', 'get in touch', 'connect'], 'phone'],
    [['blog', 'news', 'articles', 'updates'], 'file'],
    [['portfolio', 'work', 'projects', 'gallery', 'case'], 'grid'],
    [['pricing', 'plans', 'cost', 'rates'], 'dollar'],
    [['faq', 'help', 'support', 'questions'], 'help'],
    [['team', 'staff', 'people', 'members'], 'users'],
    [['menu', 'food', 'dishes', 'order'], 'clipboard'],
    [['reservation', 'booking', 'appointment', 'schedule'], 'calendar'],
    [['review', 'testimonial', 'feedback'], 'star'],
    [['location', 'map', 'directions', 'find us'], 'map'],
    [['photo', 'image', 'media'], 'camera'],
    [['safety', 'security', 'privacy'], 'shield'],
    [['search', 'find', 'browse'], 'search'],
    [['cart', 'basket', 'checkout'], 'cart'],
    [['login', 'sign in', 'account', 'profile'], 'user'],
    [['report', 'analytics', 'stats', 'data'], 'bar-chart'],
    [['settings', 'preferences', 'config'], 'settings'],
  ]

  for (const [keywords, icon] of map) {
    if (keywords.some(k => lower.includes(k))) return icon
  }
  return 'grid'
}
